#include <stdio.h>
#include <time.h>    // clock() 函數所需之標頭檔
#include <stdlib.h>  // rand(), srand() 所需標頭檔

#define N 10000      // 陣列大小，可修改此值做實驗

/* -------------------------------------------------------
 * bubble_sort：氣泡排序函式
 *   a[] : 待排序的整數陣列（傳址，會直接修改原陣列）
 *   n   : 陣列元素個數
 *
 * 演算法：
 *   外迴圈 k 從 n-1 遞減到 1
 *     每執行一輪，當輪最大值會「浮」到 a[k] 的位置
 *   內迴圈 i 從 0 到 k-1
 *     比較相鄰元素 a[i] 與 a[i+1]，若左大於右則互換
 *
 * 時間複雜度：O(n²)
 *   比較次數 = (n-1) + (n-2) + ... + 1 = n(n-1)/2
 * 空間複雜度：O(1)（原地排序，只用到一個暫存變數 t）
 * ------------------------------------------------------- */
void bubble_sort(int a[], int n)
{
    /* 外迴圈：每輪結束後，a[k..n-1] 已是最終排序結果
     * k 代表「本輪比較的最後一個位置」
     * 第 1 輪 k=n-1：比較 a[0]~a[n-1]，最大值到 a[n-1]
     * 第 2 輪 k=n-2：最大值已定，只需比較 a[0]~a[n-2]
     * 以此類推，每輪需比較的範圍縮減 1                   */
    for (int k = n - 1; k > 0; k--) {

        /* 內迴圈：比較相鄰兩個元素
         * i 的範圍是 0 ~ k-1，確保 a[i+1] 不超出邊界   */
        for (int i = 0; i < k; i++) {

            /* 若左邊元素大於右邊，則需要互換
             * 目標：由小到大排序（升冪）
             * 若要改為降冪，只需將 > 改為 <             */
            if (a[i] > a[i + 1]) {

                /* 三步驟交換（需要暫存變數 t）
                 * 不可直接寫 a[i]=a[i+1]; a[i+1]=a[i];
                 * 因為第一行執行後 a[i] 的原值已遺失     */
                int t    = a[i];      // 1. 暫存 a[i]
                a[i]     = a[i + 1]; // 2. 將 a[i+1] 覆蓋到 a[i]
                a[i + 1] = t;        // 3. 將暫存值寫入 a[i+1]
            }
        }
        /* 此時 a[k] 已是 a[0..k] 中的最大值，位置確定 */
    }
}

/* -------------------------------------------------------
 * main：複雜度實驗
 *   1. 建立「最差情況」陣列（完全逆序：N, N-1, ..., 1）
 *   2. 使用 clock() 計量排序所需 CPU 時間
 *   3. 印出結果，方便填入實驗表格
 * ------------------------------------------------------- */
int main()
{
    /* --- 1. 宣告陣列與計時變數 --- */
    int a[N];           // 待排序陣列（大小由 #define N 決定）
    clock_t start, end; // clock_t：儲存 CPU 時脈數的型別
    double cpu_time_used;

    /* --- 2. 初始化陣列為完全逆序（最差情況）---
     * a[0]=N, a[1]=N-1, ..., a[N-1]=1
     * 逆序是氣泡排序的「最差情況」：
     *   每次比較都需要互換，共進行 n(n-1)/2 次交換      */
    for (int i = 0; i < N; i++)
        a[i] = N - i;

    /* 印出前 10 個元素，確認初始狀態 */
    printf("排序前（前 10 個元素）: ");
    for (int i = 0; i < 10 && i < N; i++)
        printf("%d ", a[i]);
    printf("...\n");

    /* --- 3. 計時開始 ---
     * clock() 回傳程式從啟動到呼叫當下所消耗的 CPU 時脈數
     * 單位是 clock ticks，需除以 CLOCKS_PER_SEC 轉成秒   */
    start = clock();

    /* --- 4. 執行排序 --- */
    bubble_sort(a, N);

    /* --- 5. 計時結束 --- */
    end = clock();

    /* --- 6. 計算並輸出結果 ---
     * (end - start)：排序消耗的 clock ticks 數
     * 除以 CLOCKS_PER_SEC：轉換為秒（double 以保留小數）
     * %.4f：印出小數點後 4 位，方便比較微小差異          */
    cpu_time_used = (double)(end - start) / CLOCKS_PER_SEC;

    /* 印出後 10 個元素，確認排序正確 */
    printf("排序後（後 10 個元素）: ... ");
    for (int i = N - 10; i < N; i++)
        printf("%d ", a[i]);
    printf("\n");

    printf("排序 %d 個元素，花費時間 = %.4f 秒\n", N, cpu_time_used);

    return 0;
}

/* =======================================================
 * 實驗步驟說明
 * =======================================================
 *
 * 【步驟1】修改 #define N 的值，逐一測試：
 *   N = 1000   （基準）
 *   N = 2000
 *   N = 5000
 *   N = 10000
 *   N = 20000
 *   N = 50000
 *   N = 100000
 *
 * 【步驟2】每次修改後重新編譯執行，記錄時間：
 *
 *   | N       | 時間 (秒) | N 增加倍數 | 時間增加倍數 |
 *   |---------|-----------|------------|--------------|
 *   | 1,000   |           |    —       |      —       |
 *   | 10,000  |           |   ×10      |  預期 ×100   |
 *   | 100,000 |           |  ×100      | 預期 ×10000  |
 *
 * 【預期結果】
 *   O(n²) 代表 n 增加 10 倍，時間增加約 100 倍
 *   若實測比例接近 100，即驗證了 O(n²) 的複雜度
 *
 * ======================================================= */
