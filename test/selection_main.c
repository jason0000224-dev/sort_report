#include <stdio.h>
#include <time.h>    // clock() 函數所需之標頭檔
#include <stdlib.h>  // rand(), srand() 所需標頭檔

#define N 10000      // 陣列大小，可修改此值做實驗

/* -------------------------------------------------------
 * selection_sort：選擇排序函式
 *   a[] : 待排序的整數陣列（傳址，會直接修改原陣列）
 *   n   : 陣列元素個數
 *
 * 演算法：
 *   外迴圈 i 從 0 到 n-2
 *     每執行一輪，從 a[i..n-1] 中選出最小值，並與 a[i] 交換
 *   內迴圈 j 從 i+1 到 n-1
 *     比較 a[j] 與目前最小值，更新最小值索引
 *
 * 時間複雜度：O(n²)
 *   比較次數 = (n-1) + (n-2) + ... + 1 = n(n-1)/2
 * 空間複雜度：O(1)（原地排序，只用到一個暫存變數 t）
 * ------------------------------------------------------- */
void selection_sort(int a[], int n)
{
    for (int i = 0; i < n - 1; i++) {
        int min_idx = i;
        for (int j = i + 1; j < n; j++) {
            if (a[j] < a[min_idx]) {
                min_idx = j;
            }
        }
        if (min_idx != i) {
            int t       = a[i];
            a[i]        = a[min_idx];
            a[min_idx] = t;
        }
    }
}

/* -------------------------------------------------------
 * main：複雜度實驗
 *   1. 建立「最差情況」陣列（完全逆序：N, N-1, ..., 1）
 *   2. 使用 clock() 計量排序所需 CPU 時間
 *   3. 印出結果，方便填入實驗表格
 * ------------------------------------------------------- */
int main()
{
    /* --- 1. 宣告陣列與計時變數 --- */
    int a[N];           // 待排序陣列（大小由 #define N 決定）
    clock_t start, end; // clock_t：儲存 CPU 時脈數的型別
    double cpu_time_used;

    /* --- 2. 初始化陣列為完全逆序（最差情況）---
     * a[0]=N, a[1]=N-1, ..., a[N-1]=1
     * 逆序是氣泡排序的「最差情況」：
     *   每次比較都需要互換，共進行 n(n-1)/2 次交換      */
    for (int i = 0; i < N; i++)
        a[i] = N - i;

    /* 印出前 10 個元素，確認初始狀態 */
    printf("排序前（前 10 個元素）: ");
    for (int i = 0; i < 10 && i < N; i++)
        printf("%d ", a[i]);
    printf("...\n");

    /* --- 3. 計時開始 ---
     * clock() 回傳程式從啟動到呼叫當下所消耗的 CPU 時脈數
     * 單位是 clock ticks，需除以 CLOCKS_PER_SEC 轉成秒   */
    start = clock();

    /* --- 4. 執行排序 --- */
    selection_sort(a, N);

    /* --- 5. 計時結束 --- */
    end = clock();

    /* --- 6. 計算並輸出結果 ---
     * (end - start)：排序消耗的 clock ticks 數
     * 除以 CLOCKS_PER_SEC：轉換為秒（double 以保留小數）
     * %.4f：印出小數點後 4 位，方便比較微小差異          */
    cpu_time_used = (double)(end - start) / CLOCKS_PER_SEC;

    /* 印出後 10 個元素，確認排序正確 */
    printf("排序後（後 10 個元素）: ... ");
    for (int i = N - 10; i < N; i++)
        printf("%d ", a[i]);
    printf("\n");

    printf("排序 %d 個元素，花費時間 = %.4f 秒\n", N, cpu_time_used);

    return 0;
}

/* =======================================================
 * 實驗步驟說明
 * =======================================================
 *
 * 【步驟1】修改 #define N 的值，逐一測試：
 *   N = 1000   （基準）
 *   N = 2000
 *   N = 5000
 *   N = 10000
 *   N = 20000
 *   N = 50000
 *   N = 100000
 *
 * 【步驟2】每次修改後重新編譯執行，記錄時間：
 *
 *   | N       | 時間 (秒) | N 增加倍數 | 時間增加倍數 |
 *   |---------|-----------|------------|--------------|
 *   | 1,000   |           |    —       |      —       |
 *   | 10,000  |           |   ×10      |  預期 ×100   |
 *   | 100,000 |           |  ×100      | 預期 ×10000  |
 *
 * 【預期結果】
 *   O(n²) 代表 n 增加 10 倍，時間增加約 100 倍
 *   若實測比例接近 100，即驗證了 O(n²) 的複雜度
 *
 * ======================================================= */
